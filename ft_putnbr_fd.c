#include <unistd.h>
#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	const char	*s;

	s = "-2147483648" ;
	if (n == -2147483648)
		ft_putstr_fd((char *) s, fd);
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(-n, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(n % 10 + '0', fd);
	}
	else
		ft_putchar_fd(n + '0', fd);
}
