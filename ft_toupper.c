int	ft_toupper(int c)
{
	int					new_word;

	new_word = 1;
	if ((new_word == 1) && (c >= 'a' && c <= 'z'))
		c = c - 32;
	else if ((new_word == 0) && (c >= 'A' && c <= 'Z'))
		c = c + 32;
	if ((c < '0') || (c > '9' && c < 'A')
		|| (c > 'Z' && c < 'a') || (c > 'z'))
		new_word = 1;
	else
		new_word = 0;
	return (c);
}
